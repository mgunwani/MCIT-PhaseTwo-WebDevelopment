
// null and undefined, both are value types.
// A null means absence of value.
// An undefined means lack of value.
// A null or undefined, both evaluates to false in condition.

var value1;
console.log(value1);
console.log(value1 ? "Perfect" : "Not Perfect");

var value2 = null;
console.log(value2);
console.log(value2 ? "Superb" : "Okay");
